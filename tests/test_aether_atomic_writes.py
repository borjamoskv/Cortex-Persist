@pytest.mark.asyncio
async def test_redteam_siege_writes_test_suite_and_inits(tmp_path: Path) -> None:
    target = tmp_path / "demo.py"
    target.write_text("def add(a, b):\n    return a + b\n", encoding="utf-8")
    toolkit = AgentToolkit(tmp_path)

    class _FakeResponse:
        content = "```python\ndef test_add():\n    assert 1 + 1 == 2\n```"

    class _FakeOrchestra:
        async def think(self, prompt, mode, system):
            return _FakeResponse()

    agent = RedTeamAgent(_FakeOrchestra())

    generated = await agent.siege("Please cover demo.py before refactor", toolkit)

    assert len(generated) == 1
    test_path = tmp_path / generated[0]
    assert test_path.exists()
    assert "def test_add()" in test_path.read_text(encoding="utf-8")
    assert (tmp_path / "tests" / "autogenerated" / "__init__.py").exists()
    assert (tmp_path / "tests" / "__init__.py").exists()


def test_cdp_wallet_write_seed_persists_file_with_0600(tmp_path: Path) -> None:
    CDPSovereignWallet._instance = None
    CDPSovereignWallet._initialized = False
    try:
        wallet = CDPSovereignWallet(seed_path=str(tmp_path / "wallet" / "seed.json"))

        wallet._write_seed("seed-data")

        seed_path = tmp_path / "wallet" / "seed.json"
        assert seed_path.read_text(encoding="utf-8") == "seed-data"
        mode = stat.S_IMODE(seed_path.stat().st_mode)
        assert mode == stat.S_IRUSR | stat.S_IWUSR
    finally:
        CDPSovereignWallet._instance = None
        CDPSovereignWallet._initialized = False
