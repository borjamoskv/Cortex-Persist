def test_agent_toolkit_write_file_persists_content(tmp_path: Path) -> None:
    toolkit = AgentToolkit(tmp_path)

    result = toolkit.write_file("nested/file.py", "print('ok')\n")

    assert result == "OK — wrote 12 bytes to nested/file.py"
    assert (tmp_path / "nested" / "file.py").read_text(encoding="utf-8") == "print('ok')\n"
    source = inspect.getsource(AgentToolkit.write_file)
    assert ".write_text(" not in source


def test_agent_toolkit_write_file_rejects_escape(tmp_path: Path) -> None:
    toolkit = AgentToolkit(tmp_path)

    with pytest.raises(PermissionError):
        toolkit.write_file("../escape.txt", "blocked")


def test_agent_toolkit_direct_write_file_honors_allowed_tools(tmp_path: Path) -> None:
    toolkit = AgentToolkit(tmp_path, allowed_tools=["read_file"])

    result = toolkit.write_file("nested/file.py", "print('blocked')\n")

    assert "ToolNotAllowedError" in result
    assert not (tmp_path / "nested" / "file.py").exists()


def test_agent_toolkit_git_tools_do_not_require_bash_capability(monkeypatch, tmp_path: Path) -> None:
    toolkit = AgentToolkit(tmp_path, allowed_tools=["git"])
    captured: list[tuple[str, int]] = []

    def _fake_bash_impl(cmd: str, timeout: int) -> str:
        captured.append((cmd, timeout))
        return "M demo.py"

    monkeypatch.setattr(toolkit, "_bash_impl", _fake_bash_impl)

    result = toolkit.git_status()

    assert "M demo.py" in result
    assert captured == [("git status --short", 60)]


